def H000001():
    import random
    import time
    userinput = int(input("你想让我猜多大呢?"))
    Min = 1
    Max = userinput
    da = random.randint(0,userinput)
    time1 = time.time()
    print(f"生成的数是：{da}")
    while Min < Max:
        Sum = (Min + Max) // 2
        print(f"AI猜测的数是:{Sum}")
        if Sum == da:
            print("猜中啦！")
            break
        elif Sum < da:
            print("猜小啦")
            Min = Sum + 1
        else:
            print("猜大啦")
            Max = Sum - 1
    time2 = time.time()
    print(f"用时{time2 - time1:.20f}")
